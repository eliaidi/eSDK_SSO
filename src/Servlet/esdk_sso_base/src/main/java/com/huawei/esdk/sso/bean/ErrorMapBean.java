package com.huawei.esdk.sso.bean;

public class ErrorMapBean
{
    private String uaap;

    private String esdk;

    private String description;

    public String getUaap()
    {
        return uaap;
    }

    public void setUaap(String uaap)
    {
        this.uaap = uaap;
    }

    public String getEsdk()
    {
        return esdk;
    }

    public void setEsdk(String esdk)
    {
        this.esdk = esdk;
    }

    public String getDescription()
    {
        return description;
    }

    public void setDescription(String description)
    {
        this.description = description;
    }

}
